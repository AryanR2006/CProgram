#include <stdio.h>

int main()
 {
int x = 40;
int y = 2;

// Addition Operation
int a = x + y; 
printf("%d\n", a);

// Subtraction Operation
int b = x- y; 
printf("%d\n", b);

// Multiplication Operation
int c = x * y; 
printf("%d\n", c);

// Division Operation
int d = x / y; 
printf("%d\n", d);

// Modulus Operation
int e = x % y; 
printf("%d\n", e);

return 0;
}
